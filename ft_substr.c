#include <stdlib.h>
#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub_str;
	size_t	s_len;
	size_t	end;

	if (!s)
		return (0);
	s_len = ft_strlen(s);
	end = 0;
	if (start < s_len)
		end = s_len - start;
	if (end > len)
		end = len;
	sub_str = (char *)malloc((end + 1) * sizeof(char));
	if (!sub_str)
		return (0);
	ft_strlcpy(sub_str, s + start, end + 1);
	return (sub_str);
}
